  puts "Y: #{array_colors[2]}, \n"
  puts "G: #{array_colors[3]}, \n"
  puts "B: #{array_colors[4]}, \n"
  puts "I: #{array_colors[5]}, \n"
  puts "V: #{array_colors[6]}"
end 